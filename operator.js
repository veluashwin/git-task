// Arithmetic Operators

let a = 10;
let b = 5;

console.log(a + b);
console.log(a - b); 
console.log(a * b); 
console.log(a / b); 
console.log(a % b);
console.log(a ** 2); 

// Assignment Operators

let x = 10;

x += 5; 
console.log(x); 

x *= 2; 
console.log(x); 

//  Comparison Operators

let num1 = 10, num2 = 20;

console.log(num1 == num2);   
console.log(num1 != num2);  
console.log(num1 > num2);    
console.log(num1 < num2);    
console.log(num1 >= 10);     
console.log(num1 === "10"); 

// Logical Operators

let isTrue = true, isFalse = false;

console.log(isTrue && isFalse); 
console.log(isTrue || isFalse); 
console.log(!isTrue);      




